            <p>Metode <span>Analytical Hierarchy Process</span> (AHP) diperkenalkan oleh <strong>Thomas L. Saaty</strong> pada tahun 1980 dalam bukunya <span>“The Analytic Hierarchy Process”.</span> AHP merupakan salah satu metode untuk membantu menyusun suatu prioritas dari berbagai pilihan dengan menggunakan beberapa kriteria (multi-kriteria). Karena sifatnya yang multi kriteria, AHP cukup banyak digunakan dalam penyusunan prioritas. AHP merupakan model hierarki fungsional dengan input utamanya adalah persepsi manusia. Dengan adanya hierarki yang kompleks atau tidak terstruktur dipecah dalam sub-sub masalah kemudian disusun menjadi suatu bentuk hierarki (Irianto, 2022).</p>
            <p>Menurut Nugeraha (2017:114) mengemukakan bahwa,”AHP adalah sebuah konsep untuk pembuatan keputusan berbasis multikriteria (kriteria yang banyak). Beberapa kriteria yang dibandingkan satu dengan yang lainnya (tingkat kepentinganya) adalah penekanan utama pada konsep AHP ini.”</p>
